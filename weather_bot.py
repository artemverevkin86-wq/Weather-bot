def build_discord_embed():
    now = datetime.now(MOSCOW_TZ)
    date_str = now.strftime("%d.%m.%Y")
    
    # Выбираем случайную новость и случайный совет
    news = random.choice(NEWS)
    tip = random.choice(TIPS)
    
    # Формируем embed
    embed = {
        "title": f"🎙️ «Мир после» — {date_str}",
        "description": news,
        "color": 0x5865F2,
        "fields": [
            {
                "name": "💡 СОВЕТ ВЫЖИВШЕМУ",
                "value": tip,
                "inline": False
            },
            {
                "name": "🌐 НАШ САЙТ",
                "value": "Вся свежая информация о погоде только у нас, заходи! >>> [**сайт**](https://artemverevkin86-wq.github.io/Weather-website/)",
                "inline": False
            }
        ],
        "footer": {
            "text": "Мир после… мы ещё живы. Берегите себя."
        }
    }
    
    return embed

def send_to_discord(embed):
    if not WEBHOOK_URL:
        print("❌ DISCORD_WEBHOOK_URL не найден!")
        return False
    
    response = requests.post(WEBHOOK_URL, json={"embeds": [embed]})
    
    if response.status_code == 204:
        print("✅ Сообщение отправлено в Discord!")
        return True
    else:
        print(f"❌ Ошибка Discord: {response.status_code}")
        return False

# -------------------------------------------------------------------
# 4. ГЛАВНАЯ ФУНКЦИЯ
# -------------------------------------------------------------------
def main():
    print("🚀 Запуск бота «Мир после»...")
    
    embed = build_discord_embed()
    send_to_discord(embed)
    
    print("✅ Готово!")

if __name__ == "__main__":
    main()
