def calculate_temperature(weather):
    now = datetime.now(MOSCOW_TZ)
    month = now.month
    
    if 3 <= month <= 5:  # Весна
        ranges = {
            "Солнечная": (15, 25),
            "Дождь": (8, 15),
            "Туман": (5, 12),
            "Гроза": (12, 20),
            "Аномальная жара": (25, 35),
            "Аномальный холод": (-5, 8)
        }
    elif 6 <= month <= 8:  # Лето
        ranges = {
            "Солнечная": (22, 32),
            "Дождь": (15, 22),
            "Туман": (12, 20),
            "Гроза": (18, 28),
            "Аномальная жара": (32, 42),
            "Аномальный холод": (5, 15)
        }
    else:  # Осень/Зима
        ranges = {
            "Солнечная": (0, 10),
            "Дождь": (-3, 5),
            "Туман": (-5, 3),
            "Гроза": (-2, 6),
            "Аномальная жара": (10, 18),
            "Аномальный холод": (-20, -5)
        }
    
    temp_range = ranges.get(weather, (5, 15))
    return random.randint(temp_range[0], temp_range[1])

