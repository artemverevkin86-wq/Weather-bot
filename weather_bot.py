def get_danger_for_raion(raion_name, weather):
    raion_data = RAIONS[raion_name]
    dangers = []
    for place_type in raion_data.get("types", []):
        key = (weather, place_type)
        if key in DANGER_RULES:
            dangers.append(f"- {DANGER_RULES[key]} (район {raion_name})")
    return dangers

